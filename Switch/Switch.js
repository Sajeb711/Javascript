
// এখানে একই roject দুই ভাবে করা হয়েছে বা যায়। প্রথমে একই project ""if,else if, else"" এর মাধ্যমে করা হয়েছে । আবার একই project ""switch""  এর মাধ্যমে।        

var digit = prompt("Enter Any Digit From 1 to 20 ; ")

// switch ব্যবহার করার জন্য ৪ টা keyword সম্পর্কে জানতে হবে। switch , case, break, default. ( switch এর মধ্যে variable এর নাম বলতে হবে। তারপর case এর মধ্যে  value গুলো দিতে হবে । case লেখার পর অবশ্যই কোলন (:)  চিহ্ন দিতে হবে। case এর পর  break ব্যবহার করতে হবে। না হলে সকল value গুলোও দেখাবে।   সবার শেষের value তে case ব্যবহার না করে  default keyword ব্যবহার করতে হবে।  ) 

switch (digit) {

  case "0" :
  document.write("ZERO")
  break;

  case "1" :
  document.write("ONE")
  break;


  case "2" :
  document.write("TWO")
  break;


  case "3" :
  document.write("THREE")
  break;


  case "4" :
  document.write("FOUR")
  break;


  case "5" :
  document.write("FIVE")
  break;


  case "6" :
  document.write("SIX")
  break;


  case "7" :
  document.write("SEVEN")
  break;


  case "8" :
  document.write("EIGHT")
  break;


  case "9" :
  document.write("NINE")
  break;

  case "10" :
  document.write("TEN")
  break;

  case "11" :
  document.write("ELEVEN")
  break;

  case "12" :
  document.write("TWELVE")
  break;

  case "13" :
  document.write("THIRTEEN")
  break;

  case "14" :
  document.write("FOURTEEN")
  break;

  case "15" :
  document.write("FIFTEEN")
  break;

  case "16" :
  document.write("SIXTEEN")
  break;

  case "17" :
  document.write("SEVENTEEN")
  break;

  case "18" :
  document.write("EIGHTEEN")
  break;

  case "19" :
  document.write("NINETEEN")
  break;

  case "20" :
  document.write("TWENTY")
  break;


  default :
  document.write("Invalid Number")
}








